package com.gbceventbooking.ApprovalService.DTO;

public class UserDetails {
    private String id;
    private String name;
    private String role;;

    // Getters and setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String isRole() {
        return role;
    }

    public String getRole() {
        return role;
    }

    public String hasRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
